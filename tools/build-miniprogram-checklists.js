const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const china5aSource = JSON.parse(fs.readFileSync(path.join(root, "data", "china-5a.json"), "utf8"));
const heritageSource = JSON.parse(fs.readFileSync(path.join(root, "data", "world-heritage.json"), "utf8"));
const china5aCoordinates = JSON.parse(fs.readFileSync(path.join(root, "data", "china-5a-coordinates.json"), "utf8")).coordinates || {};
const output = path.join(root, "miniprogram", "data", "checklists.js");
const appSource = fs.readFileSync(path.join(root, "app.js"), "utf8");

function extractStringArray(source, marker) {
  const markerIndex = source.indexOf(marker);
  const arrayStart = source.indexOf("[", markerIndex);
  let depth = 0;
  let quoted = false;
  let escaped = false;
  for (let index = arrayStart; index < source.length; index += 1) {
    const character = source[index];
    if (escaped) {
      escaped = false;
      continue;
    }
    if (character === "\\") {
      escaped = true;
      continue;
    }
    if (character === '"') quoted = !quoted;
    if (quoted) continue;
    if (character === "[") depth += 1;
    if (character === "]") {
      depth -= 1;
      if (depth === 0) {
        const block = source.slice(arrayStart, index + 1);
        return [...block.matchAll(/"((?:\\.|[^"\\])*)"/gu)].map((match) => JSON.parse(`"${match[1]}"`));
      }
    }
  }
  return [];
}

const china5a = Object.entries(china5aSource.byRegion).flatMap(([region, names]) =>
  names.map((name, index) => {
    const coordinates = china5aCoordinates[name] || [];
    return {
      id: `5a-${region}-${index}`,
      name,
      area: region,
      latitude: coordinates[0],
      longitude: coordinates[1]
    };
  })
);

const worldHeritage = heritageSource.items.map((item) => ({
  id: item.id,
  name: item.zhName || item.name || item.enName,
  area: Array.isArray(item.countries) ? item.countries.join("、") : "",
  latitude: item.lat,
  longitude: item.lng
}));
const referenceLists = [
  ...["黄山", "庐山", "雁荡山"].map((name, index) => ({ id: `three-${index}`, name, area: "三山" })),
  ...["泰山", "华山", "衡山", "恒山", "嵩山"].map((name, index) => ({ id: `five-${index}`, name, area: "五岳" })),
  ...["五台山", "峨眉山", "普陀山", "九华山"].map((name, index) => ({ id: `buddhist-${index}`, name, area: "四大佛教名山" })),
  ...["莫高窟", "云冈石窟", "龙门石窟", "麦积山石窟"].map((name, index) => ({ id: `grotto-${index}`, name, area: "四大石窟" })),
  ...extractStringArray(appSource, "usNationalParks: {").map((name, index) => ({ id: `us-park-${index}`, name, area: "美国国家公园" }))
];

const content = `// Generated by tools/build-miniprogram-checklists.js\nmodule.exports = ${JSON.stringify({
  china5a,
  worldHeritage,
  referenceLists
})};\n`;

fs.mkdirSync(path.dirname(output), { recursive: true });
fs.writeFileSync(output, content, "utf8");
console.log(`Wrote ${china5a.length} 5A entries, ${worldHeritage.length} heritage entries and ${referenceLists.length} reference entries`);
